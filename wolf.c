static void				map_cpy(int tmp[MH][MW], t_wolf *e)
{
	int					i;
	int					j;

	i = 0;
	while (i < MH)
	{
		j = 0;
		while (j < MW)
		{
			e->worldmap[i][j] = tmp[i][j];
			j++;
		}
		i++;
	}
}

