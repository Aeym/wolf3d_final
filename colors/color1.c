#include "../wolf.h"

void		color6(t_wolf *e, t_draw *d)
{
	if (e->side == 1)
	{
		d->r = 0;
		d->b = 255;
		d->g = 100;
	}
	else
	{
		d->r = 0;
		d->b = 125;
		d->g = 50;
	}
}

